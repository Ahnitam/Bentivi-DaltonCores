    // Codigo da cor RAL
    private int PosicaoCodigoRAL = 0;
    // Cor RGB a ser transformada
    private int CorRGB = 0;
    // Diferença entre as cores
    private double Diferenca = 256;
    // Armazena o Nome da Cor
    private String NomeDaCor;

    public void ProcurarCor(int Cor) {
        this.PosicaoCodigoRAL = 0;
        this.CorRGB = Cor;
        this.Diferenca = 256;
        this.NomeDaCor = "";

        double DifTemp;

        for (int i = 0; i < this.NomeCores.length; i++) {
            DifTemp = Math.sqrt(
                    Math.pow(this.Vermelho[i] - Color.red(Cor), 2) +
                            Math.pow(this.Verde[i] - Color.green(Cor), 2) +
                            Math.pow(this.Azul[i] - Color.blue(Cor), 2));
            if (DifTemp < this.Diferenca) {
                this.Diferenca = DifTemp;
                this.PosicaoCodigoRAL = i;
                this.NomeDaCor = this.NomeCores[i];
            }
        }

    }

    public String getNomeDaCor() {
        return NomeDaCor;
    }
